                <h2>O que é?</h2>
                <br />
                <p>
                    O MMC (Mínimo Múltiplo Comum) como o próprio nome diz é utilizado para encontrar o menor múltiplo comum entre alguns números.
                </p>
                <br />
                <h2>O que é múltiplo?</h2>
                <br />
                <p>Um número múltiplo é uma multiplicação de um número qualquer com o número que se procura os seus números múltiplos.</p>
                <p>Exemplo para encontrar os múltiplos do número 5 basta ver sua tabuada, 5*1=5 5*2=10 ... , estes números são os múltiplos de 5.</p>
                <br />
                <h2>Quando utilizar?</h2>
                <br />
                <p>O MMC é utilizado na soma e subtração de frações, questões que envolvam números múltiplos que em algum momento se encontrarão.</p>
                <br />
                <h2>Propriedades</h2>
                <br />
                <ol>
                    <li>
                    <p>O MMC entre números primos é sempre igual ao produto(Multiplicação) entre eles.</p></li>
                    <li>
                        <p>O MMC entre números múltiplos é igual ao maior número.</p>
                    </li>
                </ol>
                <br />
                <h2>Como calcular?</h2>
                <br />
                <p>
                        O calculo do MMC é efetuado pela fatoração simultânea em números primos, onde o resultado é o produto dos números primos.
                </p>
