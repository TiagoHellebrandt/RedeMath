            <article>
                <h2>O que é?</h2>
                <br />
                <p>
                    A equação do 1º grau é uma expressão matemática que possui o sinal
                    de igualdade e uma variável.
                    Onde o objetivo é encontrar o valor da incógnita(variável).
                </p>
                <p>
                    Exemplo: 2x+3=4.
                </p>
                <br />
                <h2>Variável</h2>
                <br />
                <p>
                    A variável ou incógnita é 
                    representada por uma letra qualquer,
                    e está letra pode ter um coeficiente.
                    O coeficiente é um numero que está multiplicando a variável.
                </p>
                <br />
                <h2>Como calcular?</h2>
                <br />
                <p>
                    Para calcular uma equação do 1° grau é necessário seguir alguns passos:
                </p>
                <br />
                <ol>
                    <li>Organizar</li>
                        <ul type="none">
                            <li>
                                <p>
                                    Para organizar a equação, devemos 
                                    deixar todos os termos que apresentam a 
                                    incógnita de um lado, e os que não 
                                    possuem, do outro lado da igualdade.
                                </p>
                                <p>
                                    Exemplo: 2x+3=5-3x &rarr; 2x+3x=5-3
                                </p>
                            </li>
                        </ul>
                    <li>Somar os semelhantes</li>
                        <ul type="none">
                            <li>
                                <p>
                                    Após organizar a equação devemos somar todos os termos semelhantes.
                                </p>
                                <p>
                                    Exemplo: 2x+3x=5-3 &rarr; 5x=2
                                </p>
                            </li>
                        </ul>
                    <li>Mudar o coeficiente de lado</li>
                        <ul type="none">
                            <li>
                                <p>
                                    Após somar os semelhantes, devemos 
                                    mudar o coeficiente para o 
                                    outro lado da igualdade dividindo.
                                </p>
                                <p>
                                    Exemplo: 5x=2 &rarr; x=2&div;5 &rarr; x=0.4
                                </p>
                            </li>
                        </ul>
                </ol>
