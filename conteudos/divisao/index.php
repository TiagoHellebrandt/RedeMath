                <h2>O que é?</h2>
                <br />
                <p>
                    A divisão é uma operação matemática oposta à <a href="../multiplicacao">multiplicaçao</a>.
                    Onde o objetivo é repartir um numero em partes iguais.
                    <br /><br />
                    Exemplo: <br /><p>10 / 2 é igual a 5.<p/>

                </p>
                <br />
                <h2>Como calcular?</h2>
                <br />
                <p>
                        Para realizar um calculo de divisão é necessário armar a conta
                    que deseja calcular, após isto, da esquerda para a direita, pegue o 
                    numero maior ou igual que o divisor do dividendo e multiplique o divisor até 
                    encontrar um numero menor ou igual ao numero pego  antes ,
                    após isto subtraia-os.<br /><br />
                    
                </p>
                <p>
                    Com o resultado da subtração verifique se existe 
                    algum numero depois do numero pego, se existir abaixe-o, 
                    e repita o processo até acabar.<br /><br />
