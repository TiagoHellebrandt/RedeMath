                <h2>O que é?</h2>
                <br />
                <p>
                    A subtração é uma operação matemática oposta à <a href="../soma">adição</a>. Onde o objetivo 
                    é saber a diferença de um numero e outro.<br /><br />
                    Exemplo: 300 - 250.
                </p>
                <br />
                <h2>Como calcular?</h2>
                <br />
                <p>
                        Para realizar um calculo de subtração é necessário armar a conta
                    que deseja calcular, após isto, Subtraíra os números da direita para a esquerda.
                </p>
                <p>
                        Caso o primeiro numero for menor que o segundo, o resultado será negativo.
                    Neste caso deve-se inverter os números (exemplo: 10-20 inverte-se
                    para 20- 10),e subtraia normalmente, ao termino da conta multiplique o 
                    resultado por <b>-1</b> (um negativo).
                    <br /><br />
